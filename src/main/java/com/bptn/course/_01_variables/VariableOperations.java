package com.bptn.course._01_variables;

public class VariableOperations {

	public static void main(String[] args) {
		
		int x = 7;
		int y = 8;
		
		
		int resultAddition =  x+y;
		int resultSubtraction =  x-y;
		int resultMultiplication =  x*y;
		int resultDivision =  x/y;
		
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(resultAddition);
		System.out.println(resultSubtraction);
		System.out.println(resultMultiplication);
		System.out.println(resultDivision);
		
		x = 100;
		y= 2000;
		
		System.out.println(x);
		System.out.println(y);
		
		
		char b = 'B';
		String name = "Micah";
		
		System.out.println(b);
		System.out.println(name);
		System.out.println((char)(b+1));
		
		

		// TODO Auto-generated method stub

	}

}
